@router.post(
    "/provision/environment",
    response_model=ProvisionEnvironmentResponse,
    tags=["provisioning"],
)
async def provision_environment(request: ProvisionEnvironmentRequest):
    """
    Provision a new environment.

    This endpoint:
    1. Validates the input.
    2. Checks the environment doesn't already exist.
    3. Generates the terraform.tfvars from a template.
    4. Creates a new branch in GitHub.
    5. Commits the generated file.
    6. Raises a pull request with full details.
    """

    branch_name = request.derive_branch_name()
    file_path = f"environments/{request.environment_name}/terraform.tfvars"

    try:
        gh = GitHubService()
    except Exception:
        logger.exception("Failed to connect to GitHub")
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="Unable to connect to GitHub. Check GITHUB_TOKEN.",
        )

    # --- Guard: environment already exists ---
    if gh.environment_exists(request.environment_name):
        raise HTTPException(
            status_code=status.HTTP_409_CONFLICT,
            detail=f"Environment '{request.environment_name}' already exists in the repository.",
        )

    # --- Guard: branch already exists ---
    if gh.branch_exists(branch_name):
        raise HTTPException(
            status_code=status.HTTP_409_CONFLICT,
            detail=f"Branch '{branch_name}' already exists. Use a different Jira ticket or delete the existing branch.",
        )

    # --- Render template ---
    try:
        tfvars_content = render_tfvars(request)
    except Exception:
        logger.exception("Template rendering failed")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Failed to generate Terraform configuration.",
        )

    changes_summary = build_changes_summary(request)

    # --- Create branch ---
    try:
        gh.create_branch(branch_name)
    except ValueError as ve:
        raise HTTPException(
            status_code=status.HTTP_409_CONFLICT,
            detail=str(ve),
        )
    except Exception:
        logger.exception("Failed to create branch %s", branch_name)
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail=f"Failed to create branch '{branch_name}' in GitHub.",
        )

    # --- Commit file ---
    commit_message = (
        f"feat: add tfvars for {request.environment_name}\n\n"
        f"Auto-generated by Infra Portal.\n"
        f"Resources: bastion, VPC (primary + secondary), VPC flow logs IAM, app config mgmt"
    )

    try:
        gh.commit_file(branch_name, file_path, tfvars_content, commit_message)
    except Exception:
        logger.exception("Failed to commit file")
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="Failed to commit Terraform file to GitHub.",
        )

    # --- Update CI/CD workflows to include the new environment ---
    try:
        # Patch terraform_validate_plan.yml
        vp_content = gh.get_file_content(VALIDATE_PLAN_PATH, ref=branch_name)
        vp_patched = patch_validate_plan_workflow(
            vp_content, request.environment_name, request.aws_account_id
        )
        if vp_patched != vp_content:
            gh.update_file(
                branch_name,
                VALIDATE_PLAN_PATH,
                vp_patched,
                f"ci: add {request.environment_name} to validate/plan workflow",
            )

        # Patch terraform_plan_deploy.yml
        pd_content = gh.get_file_content(PLAN_DEPLOY_PATH, ref=branch_name)
        pd_patched = patch_plan_deploy_workflow(
            pd_content, request.environment_name, request.aws_account_id
        )
        if pd_patched != pd_content:
            gh.update_file(
                branch_name,
                PLAN_DEPLOY_PATH,
                pd_patched,
                f"ci: add {request.environment_name} to plan/deploy workflow",
            )
    except Exception:
        logger.exception("Failed to update workflow files")
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="tfvars committed, but failed to update CI/CD workflow files.",
        )

    # --- Create PR ---
    try:
        pr_url, pr_number = gh.create_pull_request(
            branch_name, request, changes_summary, file_path
        )
    except Exception:
        logger.exception("Failed to create pull request")
        raise HTTPException(
            status_code=status.HTTP_502_BAD_GATEWAY,
            detail="Branch and commit created, but failed to create the pull request.",
        )

    return ProvisionEnvironmentResponse(
        success=True,
        message=f"PR #{pr_number} created successfully for environment '{request.environment_name}'.",
        branch_name=branch_name,
        pr_url=pr_url,
        pr_number=pr_number,
        file_path=file_path,
        changes_summary=changes_summary,
    )


